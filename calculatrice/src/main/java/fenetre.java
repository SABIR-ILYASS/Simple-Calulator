    private void display(String number){
        if (lblAffichage.getText().equals("0") || operationOn){
           displayedNumber = number;
           operationOn = false;
        }
        else {
           displayedNumber = lblAffichage.getText()+number;
        }
        lblAffichage.setText(displayedNumber);
    }
    
    private boolean containDot(String number){
        return number.contains(".");
    }
    
    private void operation(Operations oper) {
        value1 = Double.parseDouble(lblAffichage.getText());
        operation = oper;
        operationOn = true;
    }
    
    private String deleteDot(String number){
        if (Double.parseDouble(number) == Math.floor(Double.parseDouble(number))){
           number = number.substring(0,number.length()-2);
        }
        return number;
    }

