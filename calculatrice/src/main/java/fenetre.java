    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        // TODO add your handling code here:
        value2 = Double.parseDouble(lblAffichage.getText());
        if (null==operation)
            displayedNumber = String.valueOf("unrecognized operation");
        else switch (operation) {
            case add:
               displayedNumber = String.valueOf(value1 + value2);
               break;
            case sub:
               displayedNumber = String.valueOf(value1 - value2);
               break;
            case multiplication:
               displayedNumber = String.valueOf(value1 * value2);
               break;
            case division:
               displayedNumber = String.valueOf(value1 / value2);
               break;
            case modulo:
               displayedNumber = String.valueOf(value1 % value2);
               break;
            case power:
               displayedNumber = String.valueOf(Math.pow(value1,value2));
               break;
        }
        lblAffichage.setText(deleteDot(displayedNumber));
        epi = true;

    }//GEN-LAST:event_btnEqualActionPerformed

